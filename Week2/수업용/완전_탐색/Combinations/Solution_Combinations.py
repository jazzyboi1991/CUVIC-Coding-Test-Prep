import sys  # 시스템 도구를 불러와서 표준 입력을 더 효율적으로 받기 위해 사용합니다.


def combine(n, k):
    """
    [combine 함수 설명]
    1부터 n까지의 숫자 중에서 k개를 뽑는 모든 가능한 조합을 찾아내는 함수입니다.
    :param n: 숫자의 범위 (1 ~ n)
    :param k: 고를 숫자의 개수
    :return: 완성된 조합들이 담긴 리스트
    """
    result = []  # 최종 조합들을 모아둘 빈 창고를 준비합니다.

    def backtrack(start, path):
        """
        [backtrack 함수 설명]
        하나씩 숫자를 골라가며 조합을 만들어가는 내부 탐색 함수입니다.
        :param start: 중복을 피하기 위해 다음으로 골라야 할 숫자의 시작값입니다.
        :param path: 현재까지 골라 놓은 숫자들을 담은 바구니입니다.
        """
        # 만약 바구니에 담긴 숫자의 개수가 목표 k개에 도달했다면 (성공!)
        if len(path) == k:
            # 현재 바구니의 내용을 그대로 복사해서 최종 창고에 저장합니다.
            # [:]를 붙이는 이유는 현재 바구니의 상태를 스냅샷처럼 찍어서 저장하기 위해서입니다.
            result.append(path[:])
            return  # 이 탐색은 완료되었으므로 이전으로 돌아갑니다.

        # 시작값(start)부터 최대 숫자(n)까지 하나씩 숫자를 넣어봅니다.
        for i in range(start, n + 1):
            # 숫자를 하나 바구니에 담습니다.
            path.append(i)

            # 다음 숫자를 고르기 위해 다시 탐색을 진행합니다.
            # 이때 방금 넣은 숫자보다 더 큰 숫자부터 고르도록 (i + 1) 설정합니다.
            backtrack(i + 1, path)

            # 탐색이 끝나고 돌아오면 다음 숫자를 넣어보기 위해 방금 넣었던 숫자를 뺍니다.
            # 이것을 '다시 되돌아간다'는 의미로 백트래킹이라고 부릅니다.
            path.pop()

    # 1번 숫자부터 비어있는 바구니([])를 들고 탐색을 시작합니다.
    backtrack(1, [])
    # 완성된 모든 조합이 담긴 창고를 돌려줍니다.
    return result


if __name__ == "__main__":
    # 프로그램이 시작되는 부분입니다.
    try:
        # 사용자로부터 n과 k 두 숫자를 한 줄로 입력받습니다.
        # map(int, ...)는 입력받은 글자들을 숫자 형태로 변환해줍니다.
        n, k = map(int, sys.stdin.readline().split())

        # 조합을 계산하는 함수를 호출합니다.
        res = combine(n, k)

        # 최종 결과를 화면에 예쁘게 출력합니다.
        print(res)

    except ValueError:
        # 만약 숫자가 아닌 엉뚱한 값을 입력했을 경우 에러 메시지를 띄웁니다.
        print("Enter the integers n and k.")
