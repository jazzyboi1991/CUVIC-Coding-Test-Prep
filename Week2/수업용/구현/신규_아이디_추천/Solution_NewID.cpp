#include <iostream> // 입출력을 담당하는 기본 라이브러리를 불러옵니다.
#include <string>   // 문자열을 다루기 위한 라이브러리를 불러옵니다.

#define endl "\n"    // 줄 바꿈 명령을 "\n"으로 짧게 줄여서 사용하겠다는 뜻입니다.

using namespace std; // 표준 라이브러리의 이름들을 편하게 사용하기 위한 설정입니다.

// 신규 아이디를 추천해주는 기능을 담당하는 함수입니다.
// 파라미터 new_id: 사용자가 처음 입력한 아이디 문자열입니다.
string solution(string new_id)
{
    // 1단계: 모든 대문자를 소문자로 바꿉니다.
    for (char &c : new_id)
    {
        c = tolower(c); // 한 글자씩 소문자로 변환합니다.
    }

    // 2단계: 알파벳 소문자, 숫자, 빼기(-), 밑줄(_), 마침표(.)를 제외한 모든 문자를 제거합니다.
    string st = "";
    for (char c : new_id)
    {
        // 소문자이거나, 숫자이거나, 허용된 특수문자(-, _, .)인 경우에만 결과에 추가합니다.
        if (islower(c) || isdigit(c) || c == '-' || c == '_' || c == '.')
        {
            st += c;
        }
    }

    // 3단계: 마침표(.)가 2번 이상 연속된 부분을 하나의 마침표로 바꿉니다.
    string temp = "";
    for (char c : st)
    {
        if (c == '.')
        {
            // 결과 문자열의 마지막 글자가 마침표라면, 현재 마침표는 무시하고 넘어갑니다.
            if (temp.length() > 0 && temp.back() == '.')
            {
                continue;
            }
        }
        temp += c; // 조건에 맞는 글자만 임시 문자열에 쌓습니다.
    }
    st = temp; // 걸러진 문자열을 다시 st에 저장합니다.

    // 4단계: 마침표(.)가 처음이나 끝에 위치한다면 제거합니다.
    if (st.front() == '.')
    {
        st.erase(st.begin()); // 맨 앞 글자가 마침표면 삭제합니다.
    }
    if (st.back() == '.')
    {
        st.pop_back(); // 맨 뒤 글자가 마침표면 삭제합니다.
    }

    // 5단계: 아이디가 빈 문자열이라면, "a"를 대입합니다.
    if (st.empty())
    {
        st = "a";
    }

    // 6단계: 아이디의 길이가 16자 이상이면, 앞의 15개 문자만 남깁니다.
    if (st.length() >= 16)
    {
        st = st.substr(0, 15); // 0번째부터 15글자만 잘라냅니다.
        // 자른 후 마지막 글자가 마침표가 되었다면 다시 제거합니다.
        if (st.back() == '.')
        {
            st.pop_back();
        }
    }

    // 7단계: 아이디의 길이가 2자 이하라면, 마지막 문자를 길이가 3이 될 때까지 반복해서 붙입니다.
    while (st.length() <= 2)
    {
        st += st.back(); // 마지막 글자를 뒤에 추가합니다.
    }

    return st; // 최종적으로 추천된 아이디를 반환합니다.
}

// 프로그램이 시작되는 메인 부분입니다.
int main()
{
    // 입출력 속도를 빠르게 하기 위한 설정입니다.
    cin.tie(NULL);
    cout.tie(NULL);
    ios_base::sync_with_stdio(false);

    string new_id;
    cout << "새로운 아이디를 입력하세요: "; // 사용자에게 입력을 요청합니다.
    cin >> new_id;                          // 사용자가 입력한 아이디를 읽어옵니다.
    // 추천된 아이디를 화면에 출력합니다.
    cout << "추천 아이디: " << solution(new_id) << "\n";
    return 0; // 프로그램을 정상적으로 종료합니다.
}