import re # 정규표현식(특정 패턴의 문자열을 찾거나 수정하는 도구)을 사용하기 위해 불러옵니다.


# 사용자가 입력한 아이디(new_id)를 규칙에 맞춰 추천 아이디로 바꿔주는 함수입니다.
def solution(new_id):
    # 1단계: 모든 대문자를 소문자로 바꿉니다.
    st = new_id.lower()
    
    # 2단계: 알파벳 소문자, 숫자, 빼기(-), 밑줄(_), 마침표(.)를 제외한 모든 문자를 제거합니다.
    # [^a-z0-9\-_.] 패턴은 '소문자, 숫자, -, _, . 이 아닌 것'을 의미합니다.
    st = re.sub('[^a-z0-9\-_.]', '', st)
    
    # 3단계: 마침표(.)가 2번 이상 연속된 부분을 하나의 마침표로 바꿉니다.
    # \.+ 패턴은 '마침표가 하나 이상 연속됨'을 의미합니다.
    st = re.sub('\.+', '.', st)
    
    # 4단계: 마침표(.)가 아이디의 처음이나 끝에 있다면 제거합니다.
    st = st.strip('.')
    
    # 5단계: 만약 아이디가 빈 문자열이라면, "a"를 대입합니다.
    if not st:
        st = "a"
    
    # 6단계: 아이디의 길이가 16자 이상이면, 앞의 15개 문자만 남깁니다.
    if len(st) >= 16:
        st = st[:15] # 15글자까지만 자릅니다.
        st = st.rstrip('.') # 자른 후 마지막 글자가 마침표라면 제거합니다.
        
    # 7단계: 아이디의 길이가 2자 이하라면, 마지막 문자를 길이가 3이 될 때까지 반복해서 붙입니다.
    while len(st) <= 2:
        st += st[-1] # 마지막 글자([-1])를 뒤에 추가합니다.
        
    return st # 완성된 추천 아이디를 반환합니다.


# 프로그램이 직접 실행될 때 작동하는 부분입니다.
if __name__ == "__main__":
    new_id = input("새로운 아이디를 입력하세요: ") # 사용자로부터 아이디를 입력받습니다.
    print(f"추천 아이디: {solution(new_id)}") # 결과를 화면에 출력합니다.
