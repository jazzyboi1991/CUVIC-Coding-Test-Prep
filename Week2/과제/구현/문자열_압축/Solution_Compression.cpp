#include <algorithm>
// C++에서 화면 입력(cin)과 출력(cout)을 담당하는 도구를 불러옵니다.
#include <iostream>
// C++에서 문자열(텍스트)을 쉽게 다루기 위한 string 도구를 불러옵니다.
#include <string>

// 화면에 출력할 때 쓰이는 줄바꿈 기호(endl)를 더 빠르게 동작하는 "\n"으로 매핑합니다.
#define endl "\n"

// 매번 std::를 입력하지 않고, 표준 라이브러리 기능들을 편리하게 사용하기 위해 선언합니다.
using namespace std;

// [문자열 압축 계산 핵심 함수]
// 전달받은 문자열을 다양한 글자 수 단위(1글자부터 전체 길이의 절반까지)로 잘라가며 압축해보고,
// 그 중 가장 짧게 줄어든 결과물의 길이를 계산해 반환합니다.
// - s: 압축할 대상이 되는 오리지널 문자열
// - 반환값: 압축을 통해 나타낼 수 있는 문자열의 가장 짧은 길이 (숫자)
int solution(string s)
{
    // 만약 문자열의 길이가 1이라면, 더 이상 쪼개거나 줄일 수 없는 상태이므로
    // 즉시 길이 1을 결과로 반환하고 함수 연산을 끝냅니다.
    if (s.length() == 1)
    {
        return 1;
    }

    // 최저 압축 길이를 찾기 위해 초기 최솟값을 원본 문자열의 전체 길이(가장 큰 값)로 설정해 둡니다.
    int answer = s.length();

    // 몇 글자 단위(step)로 쪼개어서 압축해볼지 결정하는 반복문입니다.
    // 1글자 단위부터 문자열 전체 길이의 딱 절반(length() / 2) 크기까지만 비교하면 됩니다.
    // (절반보다 크면 어차피 똑같은 크기로 반복될 수 없어 압축이 성립하지 않습니다.)
    for (int step = 1; step <= s.length() / 2; step++)
    {
        // 이번 step 크기 단위로 압축해 본 최종 결과 문자열을 차곡차곡 담아둘 상자입니다.
        string compressed = "";
        // 비교의 기준점이 될 첫 번째 문자열 조각을 0번 자리부터 step 크기만큼 잘라냅니다.
        string prev = s.substr(0, step);
        // 잘라낸 조각이 연속으로 몇 번 겹쳐 나오는지 세는 변수입니다. 초기값은 1입니다.
        int count = 1;

        // 두 번째 조각부터 시작하여 문자열의 끝까지 step 단위로 건너뛰며 조각들을 검사합니다.
        for (int j = step; j < s.length(); j += step)
        {
            // 이전에 잘라둔 기준 조각(prev)과 현재 조사하는 조각(s.substr(j, step))이 완전히 똑같은지 확인합니다.
            if (prev == s.substr(j, step))
            {
                // 글자가 완벽히 같으므로 연속 카운트를 1 올립니다.
                count++;
            }
            // 서로 다른 글자 조각이 등장했다면, 지금까지 쌓였던 압축 정보를 텍스트에 남깁니다.
            else
            {
                // 글자가 2번 이상 반복되었다면, 반복된 숫자(count)를 문자열 형태로 먼저 적어줍니다.
                if (count >= 2)
                {
                    compressed += to_string(count);
                }
                // 그 뒤에 실제 비교 조각(prev) 텍스트를 붙여줍니다.
                compressed += prev;

                // 새로운 다른 조각이 나왔으므로 기준점(prev)을 방금 확인한 새로운 조각으로 갱신합니다.
                prev = s.substr(j, step);
                // 카운트도 다시 처음 상태인 1로 돌려놓습니다.
                count = 1;
            }
        }

        // 반복문이 끝나고 마지막 조각 처리를 깜빡하면 안 되므로, 잔여 분량에 대한 압축 처리를 마저 끝냅니다.
        // 마찬가지로 2번 이상 겹쳤으면 숫자를 붙여줍니다.
        if (count >= 2)
        {
            compressed += to_string(count);
        }
        // 최종 남아있던 조각을 마저 덧붙입니다.
        compressed += prev;

        // 기존에 찾은 최소 압축 길이(answer)와 이번 단위로 완성된 결과물의 길이 중 더 짧은 길이를
        // 새로운 최적의 최소 길이로 선정해 갱신합니다. (compressed.length()의 반환형인 unsigned를 일반 int로 캐스팅해 연산합니다.)
        answer = min(answer, (int)compressed.length());
    }

    // 조사해 본 모든 압축 방법 중 가장 압도적으로 날씬해진 최저 길이를 최종 반환합니다.
    return answer;
}

// C++ 프로그램이 구동되면 제일 먼저 프로세서에 의해 호출되는 메인 진입점 함수입니다.
int main()
{
    // 압축할 문자열을 담을 변수를 생성합니다.
    string input_s;
    // 사용자에게 문자열 입력을 요구하는 안내 메시지를 보여줍니다.
    cout << "압축할 문자열을 입력하세요: ";
    // 사용자로부터 단어 입력을 받아 input_s 변수에 꽂아 넣습니다.
    cin >> input_s;

    // solution 함수를 가동하여 도출된 최종 최단 압축 길이를 화면에 보기 좋게 출력합니다.
    cout << "최소 압축 길이: " << solution(input_s) << endl;
    
    // 프로그램이 정상적으로 종료되었음을 나타내기 위해 0을 돌려줍니다.
    return 0;
}