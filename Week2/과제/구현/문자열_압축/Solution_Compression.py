# 파이썬 프로그램의 시스템 관련 기능을 사용하기 위해 sys 모듈을 가져옵니다.
import sys


# [문자열 압축 해결 함수]
# 입력받은 문자열을 1글자 단위부터 문자열 길이의 절반 단위까지 잘라가며 압축해보고,
# 그중에서 가장 짧게 압축된 결과의 길이를 찾아 돌려주는 함수입니다.
# - s: 압축할 원본 문자열 (예: "aabbaccc")
# - 반환값: 압축을 거쳐 만들 수 있는 가장 짧은 문자열의 길이 (숫자)
def solution(s):
    # 만약 입력받은 문자열의 길이가 1이라면, 더 이상 쪼개거나 압축할 수 없으므로
    # 압축할 필요 없이 즉시 길이 1을 결과로 돌려주고 함수를 끝냅니다.
    if len(s) == 1:
        return 1

    # 가장 짧은 길이를 구하기 위해 일단 초기 최솟값을 원본 문자열의 전체 길이(가장 긴 상태)로 설정해 둡니다.
    answer = len(s)

    # 문자열을 몇 글자 단위(step)로 쪼갤지 정하는 반복문입니다.
    # 1글자부터 문자열 길이의 딱 절반(길이 // 2)까지만 조사하면 됩니다.
    # (절반을 넘어서 자르면 남은 부분과 똑같은 조각이 나올 수 없어 압축이 불가능하기 때문입니다.)
    for step in range(1, len(s) // 2 + 1):
        # 이번 단계(step 단위)로 압축한 결과를 저장할 빈 텍스트 상자를 만듭니다.
        compressed = ""
        # 비교 기준이 될 첫 번째 문자열 조각을 0번 글자부터 step 글자수만큼 잘라서 저장합니다.
        prev = s[0:step]
        # 잘라낸 조각이 연속으로 몇 번 반복되는지 세어주는 계수기입니다. 처음 잘랐으므로 1에서 시작합니다.
        count = 1

        # 두 번째 조각부터 시작해서, 문자열의 끝까지 step 크기만큼 건너뛰면서 조각들을 가져와 비교합니다.
        for j in range(step, len(s), step):
            # 이전에 잘라둔 조각(prev)과 새로 가져온 조각(s[j: j + step])이 완전히 일치하는지 확인합니다.
            if prev == s[j: j + step]:
                # 똑같은 조각이 연속해서 나타났으므로 카운트를 1 증가시킵니다.
                count += 1
            # 일치하지 않는 조각이 나타났다면, 지금까지 반복된 조각 정보를 압축 텍스트에 적어줍니다.
            else:
                # 조각이 2번 이상 반복되었다면 숫자와 조각을 결합(예: "2ab")하고,
                # 한 번만 나타났다면 숫자 없이 조각(예: "ab")만 압축 텍스트 상자 뒤에 덧붙입니다.
                compressed += str(count) + prev if count >= 2 else prev
                # 비교할 기준 조각(prev)을 방금 가져온 새로운 조각으로 세대교체합니다.
                prev = s[j:j + step]
                # 반복 카운트를 다시 처음 상태인 1로 초기화합니다.
                count = 1

        # 문자열을 다 돌고 나서 마지막에 남아있던 조각에 대한 압축 처리를 마저 수행합니다.
        # 마찬가지로 반복 횟수가 2 이상이면 숫자와 조각을 결합하고, 1이면 조각만 붙여줍니다.
        compressed += str(count) + prev if count >= 2 else prev
        # 지금까지 발견된 최솟값(answer)과 이번 단계에서 만든 압축 길이(len(compressed))를 비교하여
        # 더 작은 값을 새로운 최솟값으로 갱신합니다.
        answer = min(answer, len(compressed))

    # 모든 단위로 잘라보았을 때 최종적으로 구해진 가장 짧은 길이를 돌려줍니다.
    return answer


# 프로그램이 직접 실행될 때 작동하는 시작점입니다.
if __name__ == "__main__":
    # 표준 입력(sys.stdin)에서 키보드로 입력한 한 줄을 읽어오고, 맨 끝의 줄바꿈 문자 등을 제거(.strip())합니다.
    input_str = sys.stdin.readline().strip()
    # solution 함수를 작동시켜 최소 압축 길이를 구해 출력합니다.
    print(f"최소 압축 길이: {solution(input_str)}")

