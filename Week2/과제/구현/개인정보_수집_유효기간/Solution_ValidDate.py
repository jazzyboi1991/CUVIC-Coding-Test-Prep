# 파이썬 프로그램의 시스템 관련 기능을 사용하기 위해 sys 모듈을 불러옵니다.
import sys


# [날짜 변환 함수]
# "YYYY.MM.DD" 형태의 문자열 날짜를 컴퓨터가 계산하기 쉽도록
# 1년=12달, 1달=28일 기준의 '총 일수'로 계산하여 숫자로 바꾸어 주는 함수입니다.
# - date_str: 변환할 날짜 문자열 (예: "2021.05.02")
# - 반환값: 변환된 총 일수 (숫자)
def to_days(date_str):
    # 날짜 문자열을 점('.') 기준으로 쪼갠 후, 각각 정수(숫자)로 변환하여
    # year(연도), month(월), day(일) 변수에 각각 나누어 저장합니다.
    year, month, day = map(int, date_str.split('.'))
    # 모든 달은 28일까지 있다고 가정하므로,
    # 연도에는 (12달 * 28일)을 곱하고, 월에는 28일을 곱한 뒤, 일을 더해 총 일수를 구합니다.
    return (year * 12 * 28) + (month * 28) + day


# [해결책 함수]
# 수집된 개인정보들이 오늘 날짜 기준으로 유효기간이 지났는지 확인하고,
# 파기해야 할 개인정보의 번호를 찾아서 목록으로 만드는 핵심 함수입니다.
# - today: 오늘 날짜 문자열 (예: "2022.05.19")
# - terms: 약관의 종류와 유효기간 목록 (예: ["A 6", "B 12"])
# - privacies: 수집된 개인정보의 날짜와 약관 종류 목록 (예: ["2021.05.02 A", "2021.07.01 B"])
# - 반환값: 파기해야 하는 개인정보의 번호(1부터 시작)들이 담긴 리스트
def solution(today, terms, privacies):
    # 파기해야 할 개인정보 번호들을 담아둘 빈 리스트(상자)를 만듭니다.
    answer = []
    # 오늘 날짜를 '총 일수' 형태의 숫자로 변환하여 오늘 기준일로 삼습니다.
    today_days = to_days(today)

    # 약관 정보를 쉽게 찾아 쓸 수 있도록 사전(Dictionary) 형태로 저장할 공간을 만듭니다.
    term_dict = {}
    # 약관 목록(terms)에서 정보를 하나씩 꺼내어 반복 처리합니다.
    for term in terms:
        # 공백을 기준으로 약관 종류(A, B 등)와 유효기간(개월 수)을 분리합니다.
        t_type, t_month = term.split()
        # 약관 종류를 Key(열쇠)로 하고, 개월 수를 일수(개월 수 * 28일)로 환산하여
        # Value(값)로 저장합니다.
        term_dict[t_type] = int(t_month) * 28

    # 개인정보 목록(privacies)을 순서대로 돌며 번호(0부터 시작하므로 i)와
    # 개인정보 내용(privacy)을 하나씩 가져옵니다.
    for i, privacy in enumerate(privacies):
        # 공백을 기준으로 정보 수집 날짜와 약관 종류를 분리합니다.
        p_date, p_type = privacy.split()
        # 개인정보를 수집한 날짜를 '총 일수'로 변환한 값에,
        # 해당 약관 종류에 맞는 유효기간 일수를 더해 '만료일(총 일수)'을 계산합니다.
        expire_days = to_days(p_date) + term_dict[p_type]

        # 계산된 만료일이 오늘 날짜보다 작거나 같다면 유효기간이 지난 것입니다.
        # 즉, 만료일이 오늘이거나 오늘보다 이전 날짜인 경우 파기 대상이 됩니다.
        if expire_days <= today_days:
            # 컴퓨터는 숫자를 0부터 세지만, 문제에서는 1번부터 시작하므로
            # 순서 값(i)에 1을 더해 파기 목록에 추가합니다.
            answer.append(i + 1)

    # 파기해야 할 개인정보 번호들이 담긴 최종 리스트를 반환합니다.
    return answer


# 프로그램이 직접 실행되었을 때 작동하는 시작점입니다.
if __name__ == "__main__":
    # 사용자로부터 오늘 날짜를 키보드로 입력받습니다.
    today = input("오늘 날짜 (YYYY.MM.DD): ")
    # 사용자로부터 약관 목록을 입력받아 쉼표와 공백(', ')을 기준으로 나누어 리스트로 만듭니다.
    terms_input = input("약관 목록 (예: A 6, B 12): ").split(', ')
    # 사용자로부터 개인정보 목록을 입력받아 쉼표와 공백(', ')을 기준으로 나누어 리스트로 만듭니다.
    privacies_input = input(
        "개인정보 목록 (예: 2021.05.02 A, 2021.07.01 B): ").split(', ')

    # solution 함수를 실행하여 조건에 맞는 결과를 구합니다.
    result = solution(today, terms_input, privacies_input)
    # 최종적으로 파기해야 할 개인정보 번호의 리스트를 화면에 출력합니다.
    print(f"파기해야 할 번호: {result}")
