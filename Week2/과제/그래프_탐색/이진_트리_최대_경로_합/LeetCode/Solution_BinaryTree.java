package Week2.과제.그래프_탐색.이진_트리_최대_경로_합.LeetCode;

// 문제 해결을 위한 알고리즘을 구현한 Solution 클래스입니다.
class Solution {
    // 전역적으로 트리 전체의 최대 경로 합을 저장할 변수입니다.
    // 아직 탐색을 시작하기 전이므로 자바 정수형 타입이 가질 수 있는 최솟값(Integer.MIN_VALUE)으로 초기화합니다.
    // 이는 모든 노드가 마이너스(음수) 값일 경우에도 최대 합산 결과를 정확하게 갱신하기 위함입니다.
    private int maxSum = Integer.MIN_VALUE;

    // 이진 트리의 최대 경로 합을 찾아내는 메인 함수입니다.
    // 파라미터 root: 탐색을 시작할 이진 트리의 최상단 루트 노드 객체입니다.
    // 반환값: 전체 트리 구조 내에서 얻을 수 있는 가장 큰 경로의 합산 값(정수)을 반환합니다.
    public int maxPathSum(TreeNode root) {
        // 최대 경로 합 변수(maxSum)를 안전하게 최솟값으로 재초기화합니다.
        maxSum = Integer.MIN_VALUE;
        // 최상단 루트 노드부터 시작하여 아래 자식 노드들을 깊이 우선 탐색(DFS) 방식으로 방문하고
        // 경로 연장을 통해 얻을 수 있는 이득을 계산하는 재귀 메서드를 실행합니다.
        getMaxGain(root);
        // 전체 탐색을 끝마친 후, 계산 및 비교 완료되어 저장된 최종 최대 경로 합을 돌려줍니다.
        return maxSum;
    }

    // 특정 노드를 시작점으로 하여 그 밑의 자식 노드 방향으로만 경로를 연장할 때
    // 얻을 수 있는 최대 단방향 합산 값(이득, Gain)을 구하는 재귀 메서드입니다.
    // 파라미터 node: 현재 방문 중인 트리의 특정 노드 객체입니다.
    // 반환값: 현재 노드를 출발하여 왼쪽 자식 방향이나 오른쪽 자식 방향 중 한쪽 길로만
    //        끝까지 내려갔을 때 획득할 수 있는 노드 값들의 최대 누적 합입니다.
    private int getMaxGain(TreeNode node) {
        // [탈출 조건] 현재 노드가 존재하지 않는 경우(빈 트리 자리이거나 말단 노드 아래에 도달한 경우)
        // 누적될 값이나 경로 이득이 전혀 없으므로 0을 반환하며 재귀 호출을 중단합니다.
        if (node == null)
            return 0;

        // [왼쪽 서브트리 탐색] 왼쪽 자식 방향으로 재귀 호출을 수행하여 얻을 수 있는 최대 이득을 구합니다.
        // 이때 하위 경로의 합산 결과가 음수(마이너스)라면 더해봤자 전체 합이 줄어들므로,
        // 해당 자식 방향은 전혀 연장하지 않겠다는 의미로 Math.max를 써서 최소 0으로 값을 조정합니다.
        int leftGain = Math.max(getMaxGain(node.left), 0);
        
        // [오른쪽 서브트리 탐색] 오른쪽 자식 방향으로 재귀 호출을 수행하여 최대 이득을 구합니다.
        // 마찬가지로 오른쪽 아래에서 연장해 온 경로 합이 음수라면 포함하지 않고 0으로 처리합니다.
        int rightGain = Math.max(getMaxGain(node.right), 0);

        // [현재 노드 중심의 합산] 현재 노드를 정점(산꼭대기)으로 만들고,
        // 왼쪽 자식 경로와 오른쪽 자식 경로를 모두 이어 붙이는 산 모양의 고유 경로 합을 구성합니다.
        // 산 모양 경로 합 공식 = [왼쪽 이득 + 현재 노드 자신의 값 + 오른쪽 이득]
        int currentPathSum = node.val + leftGain + rightGain;

        // [최대값 갱신] 기존에 기록되어 있던 전체 트리의 최대 경로 합(maxSum)과
        // 현재 노드를 중심으로 산 모양으로 연결해 구한 경로 합(currentPathSum)을 서로 대조하여,
        // 더 큰 값을 새로운 전체 최대 경로 합(maxSum)으로 업데이트합니다.
        maxSum = Math.max(maxSum, currentPathSum);

        // [부모에게 이득 반환] 현재 노드의 부모 노드가 자기 자신 노드와 연결해 밑으로 경로를 쭉 이어갈 수 있게 합니다.
        // 부모 관점에서는 자식들 중 단 한쪽 방향의 자식 경로만 택해서 내려가야 유효한 경로가 되므로,
        // [현재 노드 값 + 왼쪽과 오른쪽 중 더 높은 경로 가치를 지닌 자식의 이득]을 단방향 최대 합으로 반환합니다.
        return node.val + Math.max(leftGain, rightGain);
    }
}
