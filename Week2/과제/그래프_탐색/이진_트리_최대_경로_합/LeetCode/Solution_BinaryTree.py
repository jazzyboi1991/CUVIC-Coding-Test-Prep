# 이진 트리 문제를 풀기 위한 Solution 클래스입니다.
class Solution:
    # 이진 트리의 최대 경로 합을 계산하여 리턴해 주는 메인 함수입니다.
    # 파라미터 root: 탐색을 시작할 이진 트리의 최상단 루트(Root) 노드입니다.
    # 반환값: 전체 트리 안에서 노드 간의 길을 연결해 얻을 수 있는 가낭 높은 누적 가치(정수)를 반환합니다.
    def maxPathSum(self, root):
        # 전역적으로 최대 경로 합을 업데이트하고 누적 보관할 멤버 변수입니다.
        # 음수 노드만 있는 트리에서도 정상 동작할 수 있도록 파이썬에서 표현 가능한
        # 가장 작은 음수 무한대(float('-inf'))값으로 초기값을 설정해 둡니다.
        self.max_sum = float('-inf')

        # 특정 노드를 정점으로 삼고 자식 노드 방향으로 길을 따라 내려갈 때
        # 얻을 수 있는 최대 한 방향 누적 가치(이득, Gain)를 알아내는 재귀 헬퍼 함수입니다.
        # 파라미터 node: 현재 방문 중인 트리의 특정 노드 객체입니다.
        # 반환값: 현재 노드를 시작해 왼쪽과 오른쪽 서브트리 중 더 높은 가치를 갖는 자식 방향으로
        #        쭉 타고 내려가며 모을 수 있는 노드 값들의 최대 합산 결과입니다.
        def get_max_gain(node):
            # [탈출 조건] 방문 중인 노드가 아예 없는 경우(빈 노드 자리에 접근한 경우)
            # 획득할 수 있는 수치나 이득이 전혀 없으므로 0을 반환하고 함수를 마칩니다.
            if not node:
                return 0

            # [왼쪽 서브트리 탐색] 왼쪽 자식 방향으로 재귀 함수를 수행하여 얻을 수 있는 최대 이득을 구합니다.
            # 만약 하위 탐색 결과가 음수(마이너스)라면 더하는 것이 손해이므로,
            # 해당 방향으로 경로를 잇지 않기 위해(0을 얻기 위해) max 함수를 사용하여 음수 결과를 0으로 제한합니다.
            left_gain = max(get_max_gain(node.left), 0)
            
            # [오른쪽 서브트리 탐색] 오른쪽 자식 방향으로 재귀 함수를 수행하여 최대 이득을 구합니다.
            # 마찬가지로 오른쪽 아래 경로의 총합이 음수라면 합류시키지 않고 0으로 처리합니다.
            right_gain = max(get_max_gain(node.right), 0)

            # [현재 노드 기점 합산] 현재 노드를 정점(산꼭대기)으로 정해두고,
            # 왼쪽 경로 이득과 오른쪽 경로 이득을 둘 다 이어서 완전하게 꺾어지는 경로의 합을 구합니다.
            # 경로 합 = [왼쪽 이득 + 현재 노드 자신의 값 + 오른쪽 이득]
            current_path_sum = node.val + left_gain + right_gain
            
            # [전역 최대 경로 합 갱신] 지금까지 찾은 최고의 경로 합(self.max_sum)과
            # 방금 새로 조사해 본 현재 노드 기점의 경로 합(current_path_sum) 중 더 높은 값으로 갱신합니다.
            self.max_sum = max(self.max_sum, current_path_sum)

            # [부모에게 보낼 결과] 현재 노드의 부모가 자기 노드와 결합하여 경로를 계속해서 늘려나갈 수 있게 합니다.
            # 부모 입장에서는 경로 법칙에 따라 아래의 자식을 한 방향으로만 선택해 타고 내려가야 하므로,
            # [현재 노드 값 + 왼쪽과 오른쪽 자식의 최대 이득 중 더 우수한 쪽의 단방향 합산]을 반환합니다.
            return node.val + max(left_gain, right_gain)

        # 최상단 루트 노드부터 재귀 탐색을 개시합니다.
        get_max_gain(root)
        
        # 탐색이 전부 완료된 후 최종 누적되어 갱신된 최대 경로 합을 반환합니다.
        return self.max_sum
