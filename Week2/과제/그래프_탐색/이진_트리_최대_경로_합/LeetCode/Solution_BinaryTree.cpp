// 알고리즘 관련 다양한 유틸리티 함수(예: max)를 사용하기 위해 표준 라이브러리를 포함합니다.
#include <algorithm>
// 정수의 최소값(INT_MIN)을 사용하기 위해 포함합니다.
// 최대 경로 합의 초기 설정값으로 아주 작은 음수가 필요하기 때문입니다.
#include <climits>

// C++ 표준 라이브러리의 다양한 기능들(vector, max 등)을 
// std:: 접두사 없이 편리하게 사용하기 위한 설정입니다.
using namespace std;

// 문제 해결을 위한 알고리즘을 담고 있는 Solution 클래스입니다.
class Solution
{
  private:
    // 전역적으로 추적할 트리의 최대 경로 합을 저장할 변수입니다.
    // 아직 계산을 시작하기 전이므로 시스템에서 표현 가능한 가장 작은 음수(INT_MIN)로 초기화합니다.
    // 이는 모든 노드가 음수로만 이루어진 트리에서도 올바르게 최대값을 찾아 갱신하기 위함입니다.
    int max_sum = INT_MIN;

  public:
    // 이진 트리의 최대 경로 합을 계산하여 돌려주는 메인 함수입니다.
    // 파라미터 root: 탐색을 시작할 이진 트리의 최상단 루트 노드를 가리키는 포인터입니다.
    // 반환값: 전체 트리 구조 내에서 찾을 수 있는 가장 큰 경로 합산 값(정수)을 반환합니다.
    int maxPathSum(TreeNode *root)
    {
        // 최상단 루트 노드로부터 탐색을 시작하여 하위 노드들을 재귀적으로 순회하고,
        // 각 부분 경로에서 얻을 수 있는 이득(Gain)들을 계산하는 재귀 함수를 호출합니다.
        get_max_gain(root);
        
        // 전체 탐색을 끝마친 후, 최종적으로 갱신되고 기록된 전역 최대 경로 합을 반환합니다.
        return max_sum;
    }

    // 특정 노드를 시작점으로 하여 그 밑에 달린 자식 노드 방향으로만 내려갈 때
    // 얻을 수 있는 최대 단방향 합산 값(이득, Gain)을 구하는 재귀 함수입니다.
    // 파라미터 node: 현재 방문 중인 이진 트리의 특정 노드 포인터입니다.
    // 반환값: 현재 노드를 출발점으로 하여 한쪽 방향(왼쪽 혹은 오른쪽 중 하나)으로만
    //        길을 따라 내려갔을 때 확보할 수 있는 노드 값들의 최대 합산값입니다.
    int get_max_gain(TreeNode *node)
    {
        // [탈출 조건] 현재 방문할 노드가 존재하지 않는 경우(빈 트리이거나 단말 노드의 자식 자리인 경우)
        // 더 이상 얻을 수 있는 경로상의 이득이 없으므로 0을 반환하며 재귀 탐색을 종료합니다.
        if (node == NULL)
        {
            return 0;
        }

        // [왼쪽 서브트리 탐색] 왼쪽 자식 방향으로 재귀 호출을 수행하여 얻을 수 있는 최대 이득을 구합니다.
        // 만약 왼쪽 아래의 경로 합산 결과가 마이너스(음수)라면, 전체 합에 방해가 되므로
        // 아예 그쪽 길은 선택하지 않겠다는 의미로 max 함수를 사용해 최소 0으로 걸러냅니다.
        int left_gain = max(get_max_gain(node->left), 0);
        
        // [오른쪽 서브트리 탐색] 오른쪽 자식 방향으로 재귀 호출을 수행하여 얻을 수 있는 최대 이득을 구합니다.
        // 마찬가지로 오른쪽 밑에서 올라온 합산 결과가 음수라면, 연결하지 않고 0으로 처리해 버립니다.
        int right_gain = max(get_max_gain(node->right), 0);

        // [현재 노드 중심의 합산] 현재 노드를 정점(가장 꼭대기)으로 만들고,
        // 왼쪽 길과 오른쪽 길을 모두 가져와서 양쪽을 연결하는 산 모양의 고유 경로 합을 만듭니다.
        // 경로 합 공식: [왼쪽 경로 최대 이득 + 현재 노드 자신의 값 + 오른쪽 경로 최대 이득]
        int current_path_sum = node->val + left_gain + right_gain;
        
        // [최대값 갱신] 지금까지 파악했던 전체 트리의 최대 경로 합(max_sum)과
        // 방금 현재 노드를 기점으로 새로 계산한 경로 합(current_path_sum)을 서로 비교하여,
        // 더 높은 값을 새로운 전체 최대 경로 합으로 업데이트합니다.
        max_sum = max(max_sum, current_path_sum);

        // [부모에게 이득 반환] 현재 노드의 부모 노드가 자신과 연결하여 아래로 경로를 늘려나갈 수 있게 합니다.
        // 부모 관점에서는 자식들 중 한쪽 방향으로만 꺾어서 내려가야 단방향 경로가 성립되므로,
        // [현재 노드 값 + 왼쪽과 오른쪽 중 더 높은 가치를 지닌 자식의 이득]을 한 방향 최대 합으로 반환합니다.
        return node->val + max(left_gain, right_gain);
    }
};
